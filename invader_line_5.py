import pygame

class Invader50(pygame.sprite.Sprite):

    def __init__(self):
        super().__init__()
        self.health = 1
        self.max_health = 1
        self.velocity = 0
        self.image = pygame.image.load('assets/invader30.png')
        self.image = pygame.transform.scale(self.image, (60, 60))
        self.rect = self.image.get_rect()
        self.rect.x = 800
        self.rect.y = 20

    def forward(self):
        self.rect.y += self.velocity

