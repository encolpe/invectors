import pygame

class Invader40(pygame.sprite.Sprite):

    def __init__(self):
        super().__init__()
        self.health = 1
        self.max_health = 1
        self.velocity = 0
        self.image = pygame.image.load('assets/invader20.png')
        self.image = pygame.transform.scale(self.image, (50, 50))
        self.rect = self.image.get_rect()
        self.rect.x = 800
        self.rect.y = 80

    def forward(self):
        self.rect.y += self.velocity

